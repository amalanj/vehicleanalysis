package com.csc.test.vehicle.parsers;

public class MinMaxTuple {
	
	private int data;
	private int dataMin;
	private int dataMax;
	public int getData() {
		return data;
	}
	public int getDataMin() {
		return dataMin;
	}
	public int getDataMax() {
		return dataMax;
	}
	public void setData(int data) {
		this.data = data;
	}
	public void setDataMin(int dataMin) {
		this.dataMin = dataMin;
	}
	public void setDataMax(int dataMax) {
		this.dataMax = dataMax;
	}
	
}
